function ApocVeg_Vehicles.getRandomVehicle()
    if #ApocVeg_Vehicles.vehicleList == 0 then
        return nil
    end
    return ApocVeg_Vehicles.vehicleList[ZombRand(1, #ApocVeg_Vehicles.vehicleList + 1)]
end

function ApocVeg_Vehicles.getRandomDirection()
    if #ApocVeg_Vehicles.vehicleDirList == 0 then
        return nil
    end
    return ApocVeg_Vehicles.vehicleDirList[ZombRand(1, #ApocVeg_Vehicles.vehicleDirList + 1)]
end

function ApocVeg_Vehicles.isVehicleSprite(spriteName)
    if not spriteName then return false end
    return luautils.stringStarts(spriteName, "Base.") or
           luautils.stringStarts(spriteName, "85") or
           luautils.stringStarts(spriteName, "carpentry_")
end

